package Tarefas;

public class Preferencia {
	private int id;
	private int nivelPreferencia;
	private String DescricaoPreferencia;


	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getNivelPreferencia() {
		return nivelPreferencia;
	}
	public void setNivelPreferencia(int nivelPreferencia) {
		this.nivelPreferencia = nivelPreferencia;
	}
	public String getDescricaoPreferencia() {
		return DescricaoPreferencia;
	}
	public void setDescricaoPreferencia(String descricaoPreferencia) {
		DescricaoPreferencia = descricaoPreferencia;
	}
}
